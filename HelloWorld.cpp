#include <iostream>

using namespace std;

int main()
{
    cout << "Hello World" << endl;

    string name = "Gerald";
    int age = 23;
    char fInitial = 'G';
    char sInitial = 'J';
    double decimal = 2.5;

    cout << "My name is " << name << "." << endl;
    cout << "My initials are " << fInitial << sInitial << "." << endl;
    cout << "My age is currently " << age << "." << endl;
    cout << decimal << " is a decimal number." << endl;

    return 0;
}
